public class dataTypes {
    public static void main(String[] args) {
        // Primitive data type

        // for integer the default is 0
        byte b = 1; // storage = 1 byte
        short a = 1; // storage = 2 bytes
        int c = 888888888; //  storage = 4 bytes
        long d = 1; //  storage = 8 bytes

        float e = 1.1f; //  storage = 4 bytes || default value is 0.0f
        double f = 1.1; //  storage = 8 bytes || default value is 0.0d

        char g = 'a'; //  storage = 2 bytes || default value is '\u0000'
        String h = "Hello world"; //  storage = depends on the variable  || default value is null

        boolean i = true; //  storage = 1 bit || default value is false

        System.out.println(c);
    }
}
